"""Иерархия исключений для обработки ошибок формулы.

Отдельный модуль с пользовательскими исключениями упрощает обработку ошибок
в `run.py` и отделяет ожидаемые ошибки пользователя от системных сбоев.
"""

class FormulaError(ValueError):
    """Базовое исключение для всех ошибок обработки логической формулы."""


class LexerError(FormulaError):
    """Ошибка этапа лексического анализа.

    Выбрасывается, когда строку нельзя корректно разбить на токены:
    например, при пустом вводе или недопустимом символе.
    """


class ParserError(FormulaError):
    """Ошибка этапа синтаксического разбора.

    Выбрасывается, когда набор токенов не соответствует грамматике:
    например, если пропущен операнд, лишняя скобка или два оператора подряд.
    """
